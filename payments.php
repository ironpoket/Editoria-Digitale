<h2 id="apple-pay-dispositivi-ios">Apple Pay dispositivi IOS</h2>
<p>L’applicazione che rende possibili i pagamenti in mobilità è Wallet,
in italiano si traduce con portafoglio. Prima di vedere come aggiungere
una carta è bene precisare quali modelli supportano questa funzionalità.
Apple Pay è supportato da tutti i dispositivi con Touch ID (lettore di
impronte digitali) o Face ID (Riconoscimento Facciale).</p>
<p>Per aggiungere una carta a Wallet dopo aver aperto l’applicazione
sarà necessario toccare il simbolo con il + in alto a destra. Nella
nuova schermata sarà possibile scansionare la carta inquadrandola con la
fotocamera e i dati verranno presi automaticamente o si potranno
inserire manualmente; dopo l’inserimento si dovrà toccare su avanti. La
banca o l’istituto che ha emesso la carta verificherà i tuoi dati e
potrà richiedere maggiori informazioni, se necessarie per la verifica
della carta; dopo che le verifiche saranno effettuate sarà necessario
toccare un’ultima volta su avanti e la procedura sarà conclusa. Nella
home dell’app sarà visualizzata un’anteprima della carta e le attività
in ordine cronologico, ovvero gli ultimi pagamenti fatti con Apple pay;
è importante precisare che non troveremo tutte le operazioni fatte con
la carta, ma solamente quelle fatte utilizzando Apple Pay.</p>
<p><strong>Pagare con Apple Pay</strong> Le procedure da seguire sono le
seguenti, se il dispositivo supporta il Face ID premere due volte il
tasto laterale, quindi guardare lo schermo dell’iPhone per autenticarsi
oppure inserire il codice; altrimenti appoggiare il dito sul Touch ID.
In entrambi i casi poi sarà necessario tenere la parte superiore
dell’iPhone a qualche centimetro dal lettore contactless finché non si
visualizza la scritta fine e un segno di spunta sul display.</p>
<h2 id="google-pay-dispositivi-android">Google Pay dispositivi
Android</h2>
<p>Anche per i device Android l’app è Wallet scaricabile direttamente
dall’app store. Il discorso della compatibilità è un po’ più complicato
in questo caso, in quanto Android viene utilizzato da moltissimi
produttori e sono tanti i modelli di smartphone prodotti ogni anno. I
passaggi da seguire per capire se il device è compatibile sono i
seguenti.</p>
<p>Primo passaggio: Controllare lo stato di certificazione Play
Protect.</p>
<ol type="1">
<li>Aprire l’app Google Play Store .</li>
<li>Toccare Menu e di seguito Impostazioni.</li>
<li>Nella sezione “Certificazione Play Protect”, potrai controllare se
il tuo dispositivo ha la certificazione Play Protect.</li>
</ol>
<p>Secondo passaggio: Verificare se il telefono dispone della tecnologia
NFC e attivarla.</p>
<ol type="1">
<li>Aprire l’app Impostazioni sul tuo telefono Android.</li>
<li>Toccare Dispositivi collegati. Se non presente questa opzione,
cercarne una simile, come ad esempio “Wireless e reti”, “Connessioni” o
“NFC”. Potrebbe essere necessario toccare Altro per visualizzare
ulteriori impostazioni.</li>
<li>Se presente “NFC” o un’opzione simile, significa che puoi pagare nei
negozi con Google Pay.</li>
<li>Attivare NFC.</li>
</ol>
<p>Se non presente questa opzione, cercane una simile, come ad esempio
“NFC e pagamento”.</p>
<p><strong>Pagare con Google Pay</strong> La procedura è davvero
semplice basta sbloccare lo smartphone e avvicinarlo al lettore
contactless; potrebbe essere richiesto il codice di sblocco del
dispositivo o di appoggiare il dito al lettore di impronte digitali o di
verificare l’identità con Face ID, poi sarà necessario aspettare fino a
quando non comparirà un segno di spunta sul display.</p>
<h2 id="istituti-di-credito-supportati-e-sicurezza">Istituti di credito
supportati e Sicurezza</h2>
<p>In entrambe le applicazioni è possibile aggiungere tutte le carte che
vogliamo, per i pagamenti verrà utilizzata quella predefinita scelta dal
utente, purché supportate. Per vedere le carte e gli istituti di credito
supportati è necessario visitare i siti ufficiali dei due servizi.</p>
<p><strong>La sicurezza</strong> Andiamo ora ad affrontare uno dei punti
più interessanti. Le carte di credito con tecnologia contactless, che
permettono di effettuare pagamenti solamente avvicinandole al terminale
pos, possono essere clonate facilmente da malintenzionati con
dispositivi appositi; queste carte non hanno la capacità di attivarsi e
disattivarsi ad occorrenza ma sono “sempre in ascolto”, quindi il chip
per la trasmissione dei dati è sempre pronto a comunicare. La situazione
cambia decidendo di utilizzare i servizi di pagamento su smartphone in
quanto è possibile su Android disattivare la funzionalità NFC e lo
smartphone inoltre, su tutti e due i servizi, entra “in ascolto”
solamente da sbloccato e con le funzionalità NFC attive; quindi non è
possibile che i dati della carta vengano clonati quando il telefono è
bloccato e la funzionalità NFC è disattivata. Utilizzando Google Pay I
dati che vengono trasmessi al pos quando viene effettuato un pagamento
non sono i dati “veri” della carta, al commerciante viene trasmesso un
numero criptato. Nel caso lo smartphone venga rubato o sia smarrito è
possibile bloccare il dispositivo e cancellare tutti i dati presenti con
un computer o un altro dispositivo sul quale è stato effettuato il login
con l’account presente sul device perso o rubato. Per i dispositivi
Apple è possibile recarsi in questa pagina, mentre per Android la pagina
è la seguente.</p>
