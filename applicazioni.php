<p><strong><em>Come Funziona</em></strong></p>
<p>Durante il primo avvio dell’applicazione viene proposto di aggiungere
la prima carta cliccando sul pulsante con il simbolo +, che si trova in
basso a sinistra. Cliccando su di esso si accede a una nuova schermata
dove vengono proposti tutti i negozi. Per trovare facilmente il negozio
d’interesse si scrive il nome nella barra di ricerca che si trova in
alto. Su alcuni smartphone compare un avviso che chiede di consentire
all’applicazione di utilizzare la fotocamera. E’ necessario consentire
l’accesso. Ora finalmente è possibile registrare la carta, dovremmo
solamente far combaciare le linee del codice a barre con quelle della
fotocamera e poi confermare la registrazione della carta. In pochi
minuti in questo modo avremo a disposizione tutte le carte sul nostro
smartphone. Quando andremo in negozio basterà pigiare sulla icona del
negozio in cui ci troviamo e comparirà il codice a barre e sotto il
numero di carta. Con la funzione di “Assistente delle carte” e il GPS
attivo verrà suggerita in automatico la carta giusta durante lo
shopping. Abbiamo la possibilità di ordinare le carte in tre modi
diversi: in ordine alfabetico, usate di recenti e frequenza di utilizzo
maggiore.</p>
<h2 id="pharmaround">Pharmaround</h2>
<p>L’applicazione non presenta annunci e acquisti e permette di trovare
le farmacie nei dintorni attraverso la geolocalizzazione fornendo orari
e distanza; utile nel caso si sia in vacanza, ma anche per reperire
farmaci necessari in poco tempo. L’App permette di applicare dei filtri
nella ricerca ad esempiosi può chiedere che vengano mostrate solo quelle
aperte oppure è possibile impostare una distanza massima dalla posizione
corrente. Ogni farmacia ha una scheda con indirizzo, numeri di telefono,
una mappa che apre l’App di navigazione predefinita per trovare il luogo
e gli orari di apertura dei giorni che devono venire; sono presenti
anche i prodotti venduti divisi per macrocategorie e i servizi offerti
quali misurazione della pressione, prenotazione di visite specialistiche
o esami. Viene anche offerta la possibilità inversa, ovvero di filtrare
in base al servizio come la consegna a domicilio, la misurazione del
peso, elettrocardiogramma e molti altri oppure per la tipologia di
prodotti venduti. Infine ,creando un’account, è possibile prenotare il
proprio posto per usufruire dei servizi nelle farmacie
convenzionate.</p>
